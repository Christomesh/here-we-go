package main

import "fmt"

func main() {
	var number int			// Zero Value: 0
	var floating float32	// Zero Value: 0
	var boolean bool		// Zero Value: false
	var str string			// Zero Value: ""

	fmt.Println("int", number)
	fmt.Println("float32", floating)
	fmt.Println("bool", boolean)
	fmt.Println("string", str)
}
